	void testExample() {
		Registers registers = new Registers();
		
		String example = "b inc 5 if a > 1\n" + 
				"a inc 1 if b < 5\n" + 
				"c dec -10 if a >= 1\n" + 
				"c inc -20 if c == 10";
		Parser parser = new Parser(new Comparators(), new Operators());
		Stream.of(example.split("\n"))//
			.sequential()//
			.map(line -> parser.parse(line))//
			.forEach(ins -> registers.executeInstruction(ins));
		
		assertEquals(1, registers.getLargestValue());
	}

